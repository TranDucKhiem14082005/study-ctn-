# Toán tử là gì ?
# Trong Python, các toán tử là các ký hiệu được sử dụng để thực hiện các phép toán trên các giá trị hoặc biến.
# 1. Toán tử số học
# Các toán tử này được sử dụng để thực hiện các phép toán số học.
# - Cộng(+): Cộng hai số lại với nhau.
# - Trừ(-): Trừ số thứ hai từ số thứ nhất.
# - Nhân(*): Nhân hai số với nhau.
# - Chia (/): Chia số thứ nhất cho số thứ hai.
# - Chia lấy phần dư ( % ): Chia số thứ nhất cho số thứ hai và lấy phần dư.
# - Lũy thừa(**): Lũy thừa của số thứ nhất với số mũ là số thứ hai.
# - Chia lấy phần nguyên(//): Chia số thứ nhất cho số thứ hai và lấy phần nguyên của kết quả.

# 2. Toán tử so sánh
# Các toán tử này được sử dụng để so sánh hai giá trị và trả về giá trị True-False (đúng-sai)
# - Lớn hơn ( > ): Kiểm tra nếu giá trị thứ nhất lớn hơn giá trị thứ hai.
# - Nhỏ hơn ( < ): Kiểm tra nếu giá trị thứ nhất nhỏ hơn giá trị thứ hai.
# - Lớn hơn hoặc bằng ( >= ): Kiểm tra nếu giá trị thứ nhất lớn hơn hoặc bằng giá trị thứ hai.
# - Nhỏ hơn hoặc bằng ( <= ): Kiểm tra nếu giá trị thứ nhất nhỏ hơn hoặc bằng giá trị thứ hai.
# - Bằng ( == ): Kiểm tra nếu hai giá trị bằng nhau.
# - Không bằng ( != ): Kiểm tra nếu hai giá trị không bằng nhau.

# 3. Toán tử logic
# Các toán tử này được sử dụng để kết hợp các điều kiện logic.
# - Và ( and ): Trả về True nếu cả hai điều kiện đều đúng.
# - Hoặc ( or ): Trả về True nếu một trong hai điều kiện đúng.
# - Không (not): Trả về True nếu điều kiện sai và ngược lại.

# 4. Toán tử so sánh phức tạp
# Có thể kết hợp các toán tử and , or, và not để tạo ra các biểu thức logic phức tạp hơn.

